class TomlGeneratorTest {

    private fun emptyMetadata(
        versions: Map<String, ResolvedVersion> = emptyMap(),
        libraries: Map<String, EffectiveLibrary> = emptyMap(),
        plugins: Map<String, EffectivePlugin> = emptyMap(),
        bundles: Map<String, EffectiveBundle> = emptyMap(),
    ): EffectiveMetadata = EffectiveMetadata(
        schemaVersion = "1.0",
        distribution = null,
        versions = versions,
        libraries = libraries,
        plugins = plugins,
        bundles = bundles,
        diagnostics = Diagnostics.EMPTY,
        processingInfo = null,
    )

    private fun version(alias: String, value: String): ResolvedVersion =
        ResolvedVersion(alias = alias, value = value, source = VersionSource.DECLARED, originalRef = null)

    private fun library(
        alias: String,
        group: String,
        artifact: String,
        version: ResolvedVersion? = null,
        isDeprecated: Boolean = false,
        deprecation: DeprecationInfo? = null,
        isPlatform: Boolean = false,
    ): EffectiveLibrary = EffectiveLibrary(
        alias = alias,
        group = group,
        artifact = artifact,
        version = version,
        description = null,
        tags = emptySet(),
        requires = null,
        isDeprecated = isDeprecated,
        deprecation = deprecation,
        license = null,
        constraints = emptyList(),
        isPlatform = isPlatform,
    )

    private fun plugin(alias: String, id: String, version: ResolvedVersion? = null): EffectivePlugin =
        EffectivePlugin(alias = alias, id = id, version = version)

    private fun bundle(alias: String, libraries: List<String>): EffectiveBundle =
        EffectiveBundle(alias = alias, libraries = libraries)

    @Nested
    inner class EmptyMetadata {
        @Test
        fun `empty metadata with comments produces header only`() {
            val generator = TomlGenerator(TomlConfig.DEFAULT)
            val result = generator.generate(emptyMetadata())

            assertThat(result).isEqualTo("# Generated by Dependanger (toml-version-catalog)\n")
        }

        @Test
        fun `empty metadata without comments produces single newline`() {
            val config = TomlConfig.DEFAULT.copy(includeComments = false)
            val generator = TomlGenerator(config)
            val result = generator.generate(emptyMetadata())

            assertThat(result).isEqualTo("\n")
        }
    }

    @Nested
    inner class VersionsSection {
        @Test
        fun `versions section generated correctly`() {
            val generator = TomlGenerator(TomlConfig.DEFAULT.copy(includeComments = false))
            val v = version("kotlin", "2.1.20")
            val result = generator.generate(emptyMetadata(versions = mapOf("kotlin" to v)))

            assertThat(result).contains("[versions]")
            assertThat(result).contains("kotlin = \"2.1.20\"")
        }

        @Test
        fun `versions sorted alphabetically when sortSections is true`() {
            val generator = TomlGenerator(TomlConfig.DEFAULT.copy(includeComments = false))
            val versions = linkedMapOf(
                "spring-boot" to version("spring-boot", "3.4.0"),
                "kotlin" to version("kotlin", "2.1.20"),
                "assertj" to version("assertj", "3.27.3"),
            )
            val result = generator.generate(emptyMetadata(versions = versions))

            val lines = result.lines()
            val versionLines = lines.filter { it.contains(" = \"") && !it.startsWith("[") && !it.startsWith("#") }
            assertThat(versionLines).containsExactly(
                "assertj = \"3.27.3\"",
                "kotlin = \"2.1.20\"",
                "spring-boot = \"3.4.0\"",
            )
        }

        @Test
        fun `versions not sorted when sortSections is false`() {
            val config = TomlConfig.DEFAULT.copy(includeComments = false, sortSections = false)
            val generator = TomlGenerator(config)
            val versions = linkedMapOf(
                "spring-boot" to version("spring-boot", "3.4.0"),
                "kotlin" to version("kotlin", "2.1.20"),
            )
            val result = generator.generate(emptyMetadata(versions = versions))
            val versionIndex1 = result.indexOf("spring-boot")
            val versionIndex2 = result.indexOf("kotlin")
            assertThat(versionIndex1).isLessThan(versionIndex2)
        }
    }

    @Nested
    inner class LibrariesSection {
        @Test
        fun `library with version ref`() {
            val generator = TomlGenerator(TomlConfig.DEFAULT.copy(includeComments = false))
            val v = version("kotlin", "2.1.20")
            val lib = library("kotlin-stdlib", "org.jetbrains.kotlin", "kotlin-stdlib", version = v)
            val result = generator.generate(
                emptyMetadata(
                    versions = mapOf("kotlin" to v),
                    libraries = mapOf("kotlin-stdlib" to lib),
                )
            )

            assertThat(result).contains("[libraries]")
            assertThat(result).contains("kotlin-stdlib = { group = \"org.jetbrains.kotlin\", name = \"kotlin-stdlib\", version.ref = \"kotlin\" }")
        }

        @Test
        fun `library with inline version`() {
            val config = TomlConfig.DEFAULT.copy(includeComments = false, useInlineVersions = true)
            val generator = TomlGenerator(config)
            val v = version("kotlin", "2.1.20")
            val lib = library("kotlin-stdlib", "org.jetbrains.kotlin", "kotlin-stdlib", version = v)
            val result = generator.generate(
                emptyMetadata(
                    versions = mapOf("kotlin" to v),
                    libraries = mapOf("kotlin-stdlib" to lib),
                )
            )

            assertThat(result).contains("kotlin-stdlib = { group = \"org.jetbrains.kotlin\", name = \"kotlin-stdlib\", version = \"2.1.20\" }")
        }

        @Test
        fun `library without version`() {
            val generator = TomlGenerator(TomlConfig.DEFAULT.copy(includeComments = false))
            val lib = library("managed-dep", "com.example", "managed")
            val result = generator.generate(emptyMetadata(libraries = mapOf("managed-dep" to lib)))

            assertThat(result).contains("managed-dep = { group = \"com.example\", name = \"managed\" }")
        }

        @Test
        fun `library version ref fallback to inline when alias not in versions`() {
            val generator = TomlGenerator(TomlConfig.DEFAULT.copy(includeComments = false))
            val v = version("not-in-versions", "1.0.0")
            val lib = library("some-lib", "com.example", "some-lib", version = v)
            val result = generator.generate(
                emptyMetadata(
                    libraries = mapOf("some-lib" to lib),
                )
            )

            assertThat(result).contains("some-lib = { group = \"com.example\", name = \"some-lib\", version = \"1.0.0\" }")
            assertThat(result).doesNotContain("version.ref")
        }

        @Test
        fun `platform library generated as normal entry`() {
            val generator = TomlGenerator(TomlConfig.DEFAULT.copy(includeComments = false))
            val v = version("spring-boot", "3.4.0")
            val lib = library("spring-boot-bom", "org.springframework.boot", "spring-boot-dependencies", version = v, isPlatform = true)
            val result = generator.generate(
                emptyMetadata(
                    versions = mapOf("spring-boot" to v),
                    libraries = mapOf("spring-boot-bom" to lib),
                )
            )

            assertThat(result).contains("spring-boot-bom = { group = \"org.springframework.boot\", name = \"spring-boot-dependencies\", version.ref = \"spring-boot\" }")
        }
    }

    @Nested
    inner class DeprecationComments {
        @Test
        fun `deprecated library with full info`() {
            val generator = TomlGenerator(TomlConfig.DEFAULT.copy(includeComments = false))
            val v = version("spring-boot", "3.4.0")
            val lib = library(
                alias = "spring-boot-starter",
                group = "org.springframework.boot",
                artifact = "spring-boot-starter",
                version = v,
                isDeprecated = true,
                deprecation = DeprecationInfo(
                    message = "Legacy starter",
                    replacedBy = "spring-boot-starter-web",
                    since = "3.0",
                    removalVersion = "4.0",
                ),
            )
            val result = generator.generate(
                emptyMetadata(
                    versions = mapOf("spring-boot" to v),
                    libraries = mapOf("spring-boot-starter" to lib),
                )
            )

            assertThat(result).contains("# DEPRECATED: Legacy starter. Use spring-boot-starter-web instead. Removal: 4.0.")
        }

        @Test
        fun `deprecated library with partial info - no deprecation object`() {
            val generator = TomlGenerator(TomlConfig.DEFAULT.copy(includeComments = false))
            val lib = library(
                alias = "old-lib",
                group = "com.example",
                artifact = "old-lib",
                isDeprecated = true,
                deprecation = null,
            )
            val result = generator.generate(emptyMetadata(libraries = mapOf("old-lib" to lib)))

            assertThat(result).contains("# DEPRECATED.")
        }

        @Test
        fun `deprecated library with only message`() {
            val generator = TomlGenerator(TomlConfig.DEFAULT.copy(includeComments = false))
            val lib = library(
                alias = "old-lib",
                group = "com.example",
                artifact = "old-lib",
                isDeprecated = true,
                deprecation = DeprecationInfo(message = "No longer maintained", replacedBy = null, since = null, removalVersion = null),
            )
            val result = generator.generate(emptyMetadata(libraries = mapOf("old-lib" to lib)))

            assertThat(result).contains("# DEPRECATED: No longer maintained.")
        }

        @Test
        fun `deprecated library with only replacedBy`() {
            val generator = TomlGenerator(TomlConfig.DEFAULT.copy(includeComments = false))
            val lib = library(
                alias = "old-lib",
                group = "com.example",
                artifact = "old-lib",
                isDeprecated = true,
                deprecation = DeprecationInfo(message = null, replacedBy = "new-lib", since = null, removalVersion = null),
            )
            val result = generator.generate(emptyMetadata(libraries = mapOf("old-lib" to lib)))

            assertThat(result).contains("# DEPRECATED. Use new-lib instead.")
        }

        @Test
        fun `deprecation comments disabled`() {
            val config = TomlConfig.DEFAULT.copy(includeComments = false, includeDeprecationComments = false)
            val generator = TomlGenerator(config)
            val lib = library(
                alias = "old-lib",
                group = "com.example",
                artifact = "old-lib",
                isDeprecated = true,
                deprecation = DeprecationInfo(message = "Deprecated", replacedBy = "new-lib", since = null, removalVersion = "2.0"),
            )
            val result = generator.generate(emptyMetadata(libraries = mapOf("old-lib" to lib)))

            assertThat(result).doesNotContain("# DEPRECATED")
            assertThat(result).contains("old-lib = { group = \"com.example\", name = \"old-lib\" }")
        }
    }

