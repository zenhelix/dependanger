    @Nested
    inner class SpecialCharacters {
        @Test
        fun `special characters in version values are escaped`() {
            val generator = TomlGenerator(TomlConfig.DEFAULT.copy(includeComments = false))
            val v = version("special", "1.0.0-\"beta\"")
            val result = generator.generate(emptyMetadata(versions = mapOf("special" to v)))

            assertThat(result).contains("special = \"1.0.0-\\\"beta\\\"\"")
        }

        @Test
        fun `backslash in values is escaped`() {
            val generator = TomlGenerator(TomlConfig.DEFAULT.copy(includeComments = false))
            val v = version("path", "C:\\Users\\lib")
            val result = generator.generate(emptyMetadata(versions = mapOf("path" to v)))

            assertThat(result).contains("path = \"C:\\\\Users\\\\lib\"")
        }

        @Test
        fun `newlines and tabs are escaped`() {
            val generator = TomlGenerator(TomlConfig.DEFAULT.copy(includeComments = false))
            val v = version("multiline", "1.0\n2.0\t3.0")
            val result = generator.generate(emptyMetadata(versions = mapOf("multiline" to v)))

            assertThat(result).contains("multiline = \"1.0\\n2.0\\t3.0\"")
        }
    }

